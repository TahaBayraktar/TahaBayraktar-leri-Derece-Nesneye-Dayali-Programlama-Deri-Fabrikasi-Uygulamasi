    private void kaydet() {
        try {
            String an = "";
            if (jTable3.getSelectedRow() > -1) {
                tableChanged(jTable3);
            } else if (jTable4.getSelectedRow() > -1) {
                tableChanged(jTable4);
            } else {
                Yonetici y = new Yonetici();
                JTable ceoTablo = jTable3;
                JTable yoneticiTablo = jTable4;
                DefaultTableModel ceo = (DefaultTableModel) ceoTablo.getModel();
                DefaultTableModel yon = (DefaultTableModel) yoneticiTablo.getModel();
                ceo.setRowCount(0);
                yon.setRowCount(0);
                long numara = Long.parseLong(personel_numara.getText());
                an = y.İsealma(Long.parseLong(personel_tc.getText()), personel_ulke.getSelectedItem().toString(), personel_isim.getText(), personel_sifre.getText(), personel_soyad.getText(), personel_numara.getText(), Integer.parseInt(personel_maas.getText()), Integer.parseInt(personel_ort_calisma.getText()), personel_mevki.getSelectedItem().toString());
                JOptionPane.showMessageDialog(null, an, "Bilgi", JOptionPane.INFORMATION_MESSAGE);
            }

            tabloGuncelle();
            JTable ceoTablo = jTable3;
            SqlVeriIslemleriCalisan d = new SqlVeriIslemleriCalisan();
            List<Calisan> ceoVeri = d.Verileri_Listeleme();
            DefaultTableModel dfTableD = (DefaultTableModel) ceoTablo.getModel();
            dfTableD.setRowCount(0);
            for (Calisan c : ceoVeri) {
                if (c.getMevki().equals("Yönetici")) {
                    Object[] obj = {c.getTc(), "********", Character.toUpperCase(c.getUlke().charAt(0)) + c.getUlke().substring(1), Character.toUpperCase(c.getIsim().charAt(0)) + c.getIsim().substring(1), Character.toUpperCase(c.getSoyad().charAt(0)) + c.getSoyad().substring(1), c.getTelefonNo(), c.getMevki(), c.getMaas(), c.getOrtalamaCalısmaSaati()};
                    dfTableD.addRow(obj);
                }
            }
            personel_tc.setText("");
            personel_numara.setText("");
            personel_ort_calisma.setText("");
            personel_isim.setText("");
            personel_soyad.setText("");
            personel_maas.setText("");
            personel_sifre.setText("");

        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "HATA!", "Uyarı", JOptionPane.INFORMATION_MESSAGE);
            tabloGuncelle();
        }
    }
