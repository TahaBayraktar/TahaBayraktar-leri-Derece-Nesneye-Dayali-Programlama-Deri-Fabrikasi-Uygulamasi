    private void tableChanged(JTable table) {
        String an = "Girilen Bilgiler Hatalı";
        Yonetici y = new Yonetici();
        int row = table.getSelectedRow(); // Seçilen satırın indeksi
        int column = table.getSelectedColumn(); // Seçilen sütunun indeksi
        String cName = table.getColumnName(column);
        Object data = table.getValueAt(row, column); // Seçilen hücrenin değeri
        long tc = (long) table.getValueAt(table.getSelectedRow(), 0);
        if (column == 2 || column == 5 || column == 6) {
            an = y.BilgiGuncelle(tc, 0, data.toString(), cName, 0);
            JOptionPane.showMessageDialog(null, an, "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        }
        if (column == 8) {
            an = y.BilgiGuncelle(tc, (Integer) data, "", cName, 0);
            JOptionPane.showMessageDialog(null, an, "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        }
        if (column == 7) {
            an = y.MaasGuncelle(tc, (Integer) data);
            JOptionPane.showMessageDialog(null, an, "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        }

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        tabloGuncelle();

    }

