        Personel p = new Personel();
        Yonetici y = new Yonetici();

        List<Personel> veri = p.TumCalısanGoster();
        DefaultTableModel dfTable = (DefaultTableModel) tablo.getModel();
        dfTable.setRowCount(0);
        for (Personel c : veri) {
            Object[] obj = {c.getTc(), "**************", Character.toUpperCase(c.getUlke().charAt(0)) + c.getUlke().substring(1), Character.toUpperCase(c.getIsim().charAt(0)) + c.getIsim().substring(1), Character.toUpperCase(c.getSoyad().charAt(0)) + c.getSoyad().substring(1), c.getTelefonNo(), c.getMevki(), c.getMaas(), c.getOrtalamaCalısmaSaati()};
            dfTable.addRow(obj);
        }

        JTable ceoTablo = jTable3;
        Yonetici yo = new Yonetici();
        List<Calisan> ceoVeri = yo.TumCalısanGoster();
        DefaultTableModel dfTableD = (DefaultTableModel) ceoTablo.getModel();
